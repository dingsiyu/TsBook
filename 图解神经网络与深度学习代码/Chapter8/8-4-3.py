# -*- coding: utf-8 -*-
import tensorflow as tf
#"输入张量"
value=tf.constant(
        [
        #"第1个3行3列2深度的三维张量"
        [
         [[2,5],[3,3],[8,2]],
         [[6,1],[1,2],[5,4]],
         [[7,9],[2,3],[-1,3]]
         ],
         #"第2个3行3列2深度的三维张量"
         [
          [[1,3],[2,1],[3,2]],
          [[1,1],[2,2],[1,4]],
          [[3,4],[4,2],[-1,1]]
         ]
        ],tf.float32
        )
#"3个2行2列2深度的卷积核"
kernels=tf.constant(
        [
        [[[3,1,-3],[1,-1,7]],[[-2,2,-5],[2,7,3]]],
        [[[-1,3,1],[-3,-8,6]],[[4,6,8],[5,9,-5]]]
                ],tf.float32
        )
#"valid卷积"
result=tf.nn.conv2d(value,kernels,[1,1,1,1],'VALID')
#"创建会话"
session=tf.Session()
#"打印结果"
print(session.run(result))